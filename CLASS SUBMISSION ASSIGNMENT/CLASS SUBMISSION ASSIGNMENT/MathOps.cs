using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CLASS_SUBMISSION_ASSIGNMENT
{
    public class MathOperations
    {
        public void DivideByTwo(int number, out int result)
        {
            result = number / 2;
        }

        public void DivideByTwo(double number, out double result)
        {
            result = number / 2;
        }
    }
